package operator;

public class OperatorEquality {
    public static void main(String[] args) {
        int i = 6;
        int j = 7;
        System.out.println(i==6);
        System.out.println(i==7);
        System.out.println(i==j);
        System.out.println(i>j);
        System.out.println(i<j);
        System.out.println(6>7);
        System.out.println(6<7);
        System.out.println(i!=6);
        System.out.println(j!=6);
    }
}
